/* USER CODE BEGIN 4 */
void update_PID() {
    pid.error = speed - target;

    pid.filtered_error = D_FILTER_ALPHA * pid.error + (1.0 - D_FILTER_ALPHA) * pid.filtered_error;

    if (pid.error != 0) {
        pid.integral += pid.error / PID_FREQ;
        if (pid.integral > INTEGRAL_LIMIT) {
            pid.integral = INTEGRAL_LIMIT;
        } else if (pid.integral < -INTEGRAL_LIMIT) {
            pid.integral = -INTEGRAL_LIMIT;
        }
    }
    else if (target == 0){
    	pid.integral = 0;
    }

    pid.derivative = (pid.filtered_error - pid.prev_error) * PID_FREQ;
    pid.prev_error = pid.filtered_error;

    pid.raw_output = (pid.kp * pid.error + pid.ki * pid.integral + pid.kd * pid.derivative);

    pid.prev_error = pid.error;
}

void set_control(){
	if (pid.raw_output == 0) {
		pid.output = pid.raw_output;
		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, (uint32_t)(pid.output));
	}
	else{
		if (pid.raw_output > 0) {
			HAL_GPIO_WritePin(Motor_direction_GPIO_Port, Motor_direction_Pin, GPIO_PIN_SET);
			pid.output = pid.raw_output;
		} else {
			HAL_GPIO_WritePin(Motor_direction_GPIO_Port, Motor_direction_Pin, GPIO_PIN_RESET);
			pid.output = -pid.raw_output;
		}

		if (pid.output > PWM_MAX) pid.output = PWM_MAX;
		if (pid.output < PWM_MIN) pid.output = PWM_MIN;

		__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, (uint32_t)(pid.output));
	}
}

void update_encoder_position() {
	current_encoder_count = __HAL_TIM_GET_COUNTER(&htim4);

	if (current_encoder_count < ENCODER_MIDPOINT - ENCODER_MAX_VALUE){
		current_encoder_count = ENCODER_MIDPOINT - ENCODER_MAX_VALUE;
		__HAL_TIM_SET_COUNTER(&htim4, ENCODER_MIDPOINT - ENCODER_MAX_VALUE);
	}
	else if (current_encoder_count > ENCODER_MIDPOINT + ENCODER_MAX_VALUE){
		current_encoder_count = ENCODER_MIDPOINT + ENCODER_MAX_VALUE;
		__HAL_TIM_SET_COUNTER(&htim4, ENCODER_MIDPOINT + ENCODER_MAX_VALUE);
	}
}

void update_motor_speed() {
    current_motor_count = __HAL_TIM_GET_COUNTER(&htim5);

    float instant_speed;
    if (current_motor_count > MOTOR_MIDPOINT) {
        instant_speed = (float)(current_motor_count - MOTOR_MIDPOINT);
    } else {
        instant_speed = - (float)(MOTOR_MIDPOINT - current_motor_count);
    }

    __HAL_TIM_SET_COUNTER(&htim5, MOTOR_MIDPOINT);

    speed_buffer[buffer_index++] = instant_speed;
    if (buffer_index >= SPEED_FILTER_WINDOW) {
        buffer_index = 0;
    }

    float speed_sum = 0.0f;
    for (uint8_t i = 0; i < SPEED_FILTER_WINDOW; i++) {
        speed_sum += speed_buffer[i];
    }

    speed = speed_sum / SPEED_FILTER_WINDOW;
}

void update_target(){
	target = (((float) current_encoder_count) - ENCODER_MIDPOINT) / ENCODER_MAX_VALUE * MOTOR_MAX_VEL;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
    if (htim->Instance == TIM1) {

    	update_encoder_position();
    	update_motor_speed();
    	update_target();

    	update_PID();

    	set_control();

    	simulink_packet.speed[0] = target;
    	simulink_packet.speed[1] = speed;
    	simulink_packet.control = (uint32_t) pid.output;
    	HAL_UART_Transmit_IT(&huart2, (uint8_t*) &simulink_packet, sizeof(SimulinkPacket));
    }
}
